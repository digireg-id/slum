                            <p>
                                Selamat datang di website kami. Perkenalkanlah, nama saya Jati, dan saat ini saya sedang melakukan penelitian PhD saya di University of Twente, Belanda. Penelitian kami berfokus untuk memahami bagaimana latar seseorang dapat memengaruhi cara mereka dalam memahami permukiman kumuh melalui citra satelit. Untuk informasi lebih detail tentang penelitian saya, Anda dapat mengunjungi tautan ini <a href="https://www.itc.nl/research/phd-projects/!/387749/framework-development-in-measuring-the-transferability-of-obia-based-slum-detection" style="color:blue" target="_blank">ITC | PhD research projects in geo-information science and earth observation</a>.
                            </p>
                            <p>
                                Seperti yang kita ketahui, penanganan masalah permukiman kumuh telah menjadi agenda global dalam Sustainable Development Goals (SDGs). Namun, dalam memantau perubahan permukiman kumuh yang sangat dinamis, guna memantau efektivitas program perbaikan atau pencegahan permukiman kumuh sangatlah menantang. Hanya dengan mengandalkan survei lapangan akan memakan waktu yang lama, serta jumlah uang yang besar. Penginderaan jauh menawarkan beberapa kelebihan diantaranya melalui penyediaan informasi ter update melalui pemanfaatan citra satelit resolusi sangat tinggi.
                            </p>
                            <span id="more2" style="display:none">
                            <p>
                                Meskipun demikian, beberapa masalah tetap ada. Keberadaan kampung yang memiliki kesamaan karakteristik dengan kawasan kumuh, seperti kepadatan yang tinggi dan pola permukiman yang tidak teratur membuat kita sulit untuk membedakan antara kawasan kumuh dan non-kumuh. Selain itu, latar belakang dan pengetahuan yang berbeda terhadap daerah kumuh dalam konteks lokal dapat mempengaruhi cara kita mengidentifikasi daerah kumuh. Oleh karena itu, penelitian kami bermaksud untuk memahami bagaimana perbedaan latar belakang dan pengetahuan tentang daerah kumuh dapat mempengaruhi bagaimana kami mengidentifikasi daerah kumuh dari citra satelit. Penelitian kami diharapkan dapat memberikan pemahaman yang lebih baik tentang unsur ketidakpastian yang bersumber dari aspek manusia, dalam membuat data referensi tentang permukiman kumuh. Pemahaman ini sangat penting untuk mendukung pengembangan deteksi kawasan kumuh otomatis berdasarkan algoritma pembelajaran mesin (machine-learning), yang akan membantu pengambil keputusan membuat kebijakan terkait kawasan kumuh yang lebih cepat dan akurat.
                            </p>

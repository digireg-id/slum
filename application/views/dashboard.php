                            <h3 class="mb-10 font-weight-bold text-dark">Introduction</h3>
                            <p>
                                Greeting and welcome to the website. My name is Jati, and currently, I pursuing my PhD at the University of Twente, Netherlands. Our research focuses on understanding how our background affects how we perceive informal settlements from satellite imagery. For more detailed information on my research, you can visit this link <a href="https://www.itc.nl/research/phd-projects/!/387749/framework-development-in-measuring-the-transferability-of-obia-based-slum-detection" style="color:blue" target="_blank">ITC | PhD research projects in geo-information science and earth observation</a>.
                            </p>
                            <p>
                                As we know, tackling the slums issue has become a global agenda in the Sustainable Development Goals (SDG). However, monitoring the dynamic changes of slums, which will be used to monitor the effectiveness of slums improvement or prevention programs, is challenging. Relying on the field survey may take a longer time, also a massive amount of money. Remote-sensing, however, offers a better opportunity due to the almost-realtime image acquired from the satellite imagery with very high resolutions.
                            </p>
			    <span id="more1" style="display:none">
                            <p>
                                Nonetheless, some problems remain. The existence of an urban village (kampungs), which shares similar characteristics as a slum, such as high density and irregular pattern, made it difficult for us to distinguish between slum and non-slum areas. Also, different backgrounds and knowledge towards slums in a local context may affect how we indicate the slum. Therefore, our study will understand how different backgrounds and knowledge of slums may affect how we indicate the slums from the imagery. Our research will give a better understanding of all human-aspect uncertainty on creating reference data regarding slums. This understanding is critical to support the development of automated slum detections based on machine learning algorithms, which will help the decision-maker make faster and more accurate slum-related policies.
                            </p>
                            <p>
                                Within the next maximum of 10 minutes, you will be asked several questions such as:
                            </p>
                            <ol>
                                <li>Background and knowledge of Slums in Jakarta.</li>
                                <li>Next, you will be asked to make slum-delineations in three areas by utilizing Worldview-3 imagery with 0.4-meter image resolution.</li>
                                <li>For each image, you will be asked why you indicate such an area as a slum and whether you know or not this area.</li>
                                <li>You can draw as much polygon as you want within each question as long as it is within the area given in the images. For each polygon, you can mention different indicators. The total number of drawing are mentioned for each question. For this, you can review your answer, edit or remove it before you go to the following questions.</li>
                                <li>After you successfully draw the area for every question, you can submit the result.</li>
                            </ol>
